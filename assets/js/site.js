/* assets/js/site.js
   Public blog renderer that fetches '/posts.json' (generated by GitHub Action)
   and provides search + tag filtering.
*/

/* -------------------------
   NAV CONFIG (edit as desired)
   ------------------------- */
const navConfig = [
  {
    title: 'Menu One',
    items: [
      { name: 'Home', href: 'index.html' },
      { name: 'About', href: 'about.html' }
    ]
  },
  {
    title: 'Menu Two',
    items: [
      { name: 'Blog', href: 'blog.html' }
    ]
  },
  {
    title: 'Menu Three',
    items: [
      { name: 'Contact', href: '#' }
    ]
  }
];

function initNav() {
  const target = document.getElementById('nav-menus');
  if (!target) return;
  target.innerHTML = '';
  navConfig.forEach((menu, i) => {
    const li = document.createElement('li');
    li.className = 'nav-item dropdown';
    const a = document.createElement('a');
    a.className = 'nav-link dropdown-toggle';
    a.href = '#';
    a.id = `dropdown-${i}`;
    a.setAttribute('role', 'button');
    a.setAttribute('data-bs-toggle', 'dropdown');
    a.setAttribute('aria-expanded', 'false');
    a.textContent = menu.title;

    const ul = document.createElement('ul');
    ul.className = 'dropdown-menu dropdown-menu-end';
    ul.setAttribute('aria-labelledby', `dropdown-${i}`);

    menu.items.forEach(item => {
      const itemLi = document.createElement('li');
      const itemA = document.createElement('a');
      itemA.className = 'dropdown-item';
      itemA.href = item.href || '#';
      itemA.textContent = item.name;
      itemLi.appendChild(itemA);
      ul.appendChild(itemLi);
    });

    li.appendChild(a);
    li.appendChild(ul);
    target.appendChild(li);
  });
}

/* -------------------------
   BLOG APP (public)
   ------------------------- */
const BlogApp = (function () {
  let postsCache = [];
  let sourcePath = '/posts.json';

  function formatDateISO(dateStrOrMs) {
    if (!dateStrOrMs) return '';
    const d = new Date(dateStrOrMs);
    return d.toLocaleString();
  }

  async function fetchPosts() {
    try {
      const res = await fetch(sourcePath, { cache: 'no-cache' });
      if (!res.ok) throw new Error('Failed to load posts.json');
      const data = await res.json();
      // expect array with { id, title, content (HTML), tags, date }
      postsCache = Array.isArray(data) ? data : [];
      // ensure sorted newest-first (by date or createdAt)
      postsCache.sort((a,b) => {
        const da = new Date(a.date || a.createdAt || 0).getTime();
        const db = new Date(b.date || b.createdAt || 0).getTime();
        return db - da;
      });
    } catch (err) {
      console.error('Error fetching posts:', err);
      postsCache = [];
    }
  }

  function renderPosts(filtered) {
    const container = document.getElementById('posts-list');
    const noPosts = document.getElementById('no-posts');
    if (!container) return;
    const list = filtered || postsCache;

    if (!list || list.length === 0) {
      container.innerHTML = '';
      if (noPosts) noPosts.style.display = '';
      return;
    }
    if (noPosts) noPosts.style.display = 'none';

    container.innerHTML = '';
    list.forEach(post => {
      const card = document.createElement('div');
      card.className = 'card mb-3';
      const body = document.createElement('div');
      body.className = 'card-body';

      const h = document.createElement('h4');
      h.className = 'card-title';
      h.textContent = post.title || 'Untitled';

      const meta = document.createElement('div');
      meta.className = 'text-muted small mb-2';
      meta.textContent = formatDateISO(post.date || post.createdAt);

      const content = document.createElement('div');
      content.className = 'card-text';
      // posts.json content is pre-rendered HTML from markdown -> safe for this use
      content.innerHTML = (post.content && post.content.length > 800) ? post.content.slice(0,800) + '...' : (post.content || '');

      body.appendChild(h);
      body.appendChild(meta);
      body.appendChild(content);

      if (Array.isArray(post.tags) && post.tags.length) {
        const tagsWrap = document.createElement('div');
        tagsWrap.className = 'mt-2';
        post.tags.forEach(t => {
          const tagBtn = document.createElement('button');
          tagBtn.className = 'btn btn-sm btn-outline-secondary me-1 mb-1';
          tagBtn.textContent = t;
          tagBtn.onclick = () => {
            const filteredPosts = postsCache.filter(p => (p.tags || []).includes(t));
            highlightActiveFilter(t);
            renderPosts(filteredPosts);
            window.scrollTo({ top: 0, behavior: 'smooth' });
          };
          tagsWrap.appendChild(tagBtn);
        });
        body.appendChild(tagsWrap);
      }

      card.appendChild(body);
      container.appendChild(card);
    });
  }

  function highlightActiveFilter(tag) {
    const container = document.getElementById('active-filters');
    if (!container) return;
    container.innerHTML = '';
    if (!tag) return;
    const badge = document.createElement('span');
    badge.className = 'badge bg-secondary me-2';
    badge.textContent = 'Filtering: ' + tag + ' ';
    const clearBtn = document.createElement('button');
    clearBtn.className = 'btn btn-sm btn-light ms-2';
    clearBtn.textContent = 'Clear';
    clearBtn.onclick = () => {
      document.getElementById('search-input').value = '';
      renderPosts();
      container.innerHTML = '';
    };
    container.appendChild(badge);
    container.appendChild(clearBtn);
  }

  function applySearchAndRender() {
    const q = (document.getElementById('search-input')?.value || '').toLowerCase().trim();
    if (!q) {
      renderPosts();
      return;
    }
    const filtered = postsCache.filter(p => {
      return (p.title||'').toLowerCase().includes(q) || (stripHtml(p.content||'').toLowerCase().includes(q));
    });
    renderPosts(filtered);
  }

  function stripHtml(html) {
    const tmp = document.createElement('div');
    tmp.innerHTML = html;
    return tmp.innerText || tmp.textContent || '';
  }

  return {
    init: async function (opts = {}) {
      if (opts.source) sourcePath = opts.source;
      await fetchPosts();
      renderPosts();

      const searchInput = document.getElementById('search-input');
      if (searchInput) {
        searchInput.addEventListener('input', () => applySearchAndRender());
      }
    }
  };
})();